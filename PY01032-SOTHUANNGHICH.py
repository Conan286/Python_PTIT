def checkPal(s):
    l = 0
    r = len(s) - 1
    while l < r:
        if s[l] != s[r]: return 0
        l += 1
        r -= 1
    return 1


def cs(n, k):
    s = ""
    while n:
        s = str(n % k) + s
        n //= k
    return checkPal(s)


def NumOfAns(a, b, m):
    cnt = 0
    for i in range(a, b + 1):
        check = 1
        for k in range(2, m + 1):
            if cs(i, k) == 0:
                check = 0
                break
        if check:
            cnt += 1
    return cnt


